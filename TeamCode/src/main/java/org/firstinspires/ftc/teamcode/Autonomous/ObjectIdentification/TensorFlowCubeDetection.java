    private int _CubePos = 0;
    private Semaphore mySemaphore = new Semaphore(1,true);
    public int GetCubePos()
    {
        int CubePos = -1;
        try {
            mySemaphore.acquire();

            CubePos = _CubePos;
        }
        catch (InterruptedException e) {
            e.printStackTrace();
        } finally
        {
            mySemaphore.release();
        }
        return CubePos;
    }
    public void SetCubePos(int value)
    {
        try {
            mySemaphore.acquire();

            _CubePos = value;
        }
        catch (InterruptedException e) {
            e.printStackTrace();
        } finally {
            mySemaphore.release();
        }
    }


